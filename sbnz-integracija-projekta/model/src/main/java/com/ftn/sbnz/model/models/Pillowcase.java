package com.ftn.sbnz.model.models;

public class Pillowcase {
    private int length;
    private int width;

    public Pillowcase(int length, int width) {
        this.length = length;
        this.width = width;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
